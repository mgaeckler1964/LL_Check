class ImportStatus
{
	protected:
	const char		*lastStatus;
	unsigned int	lastSecond;
	int				lastPercent;

	public:
	ImportStatus()
	{
		lastStatus = NULL;
		lastSecond = 0;
		lastPercent = -1;
	}
	virtual void ShowStatus(
		const char *serverName, const char *fileName, int percent,
		int curPos, int fileSize,
		int records,
		int errors,
		const char *status,
		clock_t startTicks
	) = 0;
	virtual void ShowServer( const AnsiString &serverName ) = 0;
	virtual void ShowFile( const AnsiString &fileName ) = 0;
	virtual void ShowStatus( const AnsiString &status ) = 0;
};

